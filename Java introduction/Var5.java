package variable;

public class Var5 {
    public static void main(String[] args) {
        int a; //선언
        a = 10; //초기화
        System.out.println(a);

        int b = 20; //변수 선언과 초기화를 한번에
        System.out.println(b);

        int c = 3, d = 4, e = 5; //여러 변수 선언과 초기화 한번에
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
